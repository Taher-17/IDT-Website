import { AppItem } from "../types/AppItem";

function createAppItem(
  item: Partial<AppItem> & {
    id: string;
    path: string;
    title: string;
    subtitle: string;
    description: string;
    iconURL: string;
    heroURL: string;
  }
): AppItem {
  return {
    ...item,
    linkURL: item.linkURL ?? `https://apps.apple.com/app/id${item.id}`,
    categories: item.categories,
    tags: item.tags,
  };
}

